<template>
    <generic-crud-data-table :config="config" />
</template>

<script lang="ts">
import {
    thesisCommitteeService,
    thesisService,
    professorService,
    placeService,
} from 'src/services';
import { defineComponent, ref } from 'vue';
import { GenericCrudTableConfig } from '../../genericCrudTable/models/table.model';
import GenericCrudDataTable from '../../genericCrudTable/views/GenericCrudDataTable.vue';

export default defineComponent({
    components: { GenericCrudDataTable },
    name: 'thesisCommitteeHandler',
    props: {},
    emits: [],
    setup(props, { emit }) {
        const config = ref<GenericCrudTableConfig>({
            name: 'Tribunales de Tesis',
            singularLabel: 'Tribunal',
            service: thesisCommitteeService,
            fields: [
                {
                    name: 'date',
                    label: 'Fecha',
                    type: 'date',
                },
                {
                    name: 'time',
                    label: 'Hora',
                    type: 'time',
                },
                {
                    name: 'place',
                    label: 'Lugar',
                    column: {
                        transform(row) {
                            return `${row.place.name}`;
                        },
                    },
                    type: 'select',
                    selectOptions: {
                        list: placeService.list,
                        value: 'id',
                        label: 'name',
                    },
                    rules: ['required'],
                },
                {
                    name: 'thesis',
                    label: 'TÃ­tulo',
                    column: {
                        transform(row) {
                            return `${row.thesis.title}`;
                        },
                    },
                    type: 'select',
                    selectOptions: {
                        list: thesisService.list,
                        value: 'id',
                        label: 'title',
                    },
                    rules: ['required'],
                },
                {
                    name: 'student',
                    label: 'Estudiante',
                    column: {
                        transform(row) {
                            return `${row.thesis.student}`;
                        },
                    },
                },
                {
                    name: 'tutor',
                    label: 'Tutor(es)',
                    column: {
                        transform(row) {
                            var result =
                                row.thesis.tutor.name +
                                ' ' +
                                row.thesis.tutor.last_name +
                                ', ';

                            row.thesis.cotutors.forEach((tutor: any) => {
                                result +=
                                    tutor.name + ' ' + tutor.last_name + ', ';
                            });
                            return `${result}`;
                        },
                    },
                },
                {
                    name: 'president',
                    label: 'Presidente',
                    column: {
                        transform(row) {
                            return `${
                                row.president.name +
                                ' ' +
                                row.president.last_name
                            }`;
                        },
                    },
                    type: 'select',
                    selectOptions: {
                        list: professorService.list,
                        value: 'id',
                        label: 'name',
                    },
                    rules: ['required'],
                },
                {
                    name: 'secretary',
                    label: 'Secretario',
                    column: {
                        transform(row) {
                            return `${
                                row.secretary.name +
                                ' ' +
                                row.secretary.last_name
                            }`;
                        },
                    },
                    type: 'select',
                    selectOptions: {
                        list: professorService.list,
                        value: 'id',
                        label: 'name',
                    },
                    rules: ['required'],
                },
                {
                    name: 'opponent',
                    label: 'Oponente',
                    column: {
                        transform(row) {
                            return `${
                                row.opponent.name + ' ' + row.opponent.last_name
                            }`;
                        },
                    },
                    type: 'select',
                    selectOptions: {
                        list: professorService.list,
                        value: 'id',
                        label: 'name',
                    },
                    rules: ['required'],
                },
            ],
            actions: {
                create: true,
                update: true,
                delete: true,
            },
        });
        return { config };
    },
});
</script>
